                                <h4>TREKKING</h4>

                                <p><small> You Can Walk, You Can Do It....</small></p>

                                <p>Of the twenty seven districts in the state of Karnataka; Kodagu covers a total area
                                    of 4102 Sq. Km. Extending from 75° 38’ E - 76° 14’ E longitude to 11° 56’ N - 12°
                                    50’ N latitude; Kodagu or commonly called Coorg has an annual rainfall close to 3000
                                    mm. Almost the whole of the Kodagu district comes under the Western Ghats, which is
                                    one of the 34 environmentally undisturbed eco systems of the world, harbouring a
                                    rich biodiversity in its flora & fauna. A typical feature of the sloping Western
                                    Ghats are the isolated ever green or shola forests nestled between the folds of the
                                    mountain ranges, amidst of these ranges lies the unique ‘The Tamara Coorg’ at an
                                    elevation of 3750 ft. surrounded by the Western Ghats ranges.</p>

                                <p>There are three trekking trails leading towards these ranges viz. as follows</p>

                                <p><strong> View Point:</strong>

                                    Approximate duration of this trek is 3.5 Hrs Distance of this trail is 4.5 Km. One
                                    can enjoy this trek through the Cardamom Plantations and can observe beautiful birds
                                    (around 310 species of birds in Coorg) and their activities, trying to understand
                                    them and documenting these observations, constitute bird watching. Approximate
                                    duration of this trek is 3.5 Hrs. consisting of two rest breaks of Ten minutes each.
                                </p>

                                <p><strong>Pathi Pole Falls:</strong>

                                    Approximate duration of this trek is 4.5 Hrs, Amidst the valley raises the shola
                                    forest from where the Pathi Pole Falls emerges, it is a great sight from the boulder
                                    resting area. This 5.5 Km of trail takes you through breath taking slopes and
                                    valleys with lush greenery. One will have a wonderful sight atop the falls. Duration
                                    of this trek is approximately 4.5 Hrs. with three breaks.</p>

                                <p><strong>Ballyaatre Ridge:</strong>

                                    Approximate duration of this trek is 5.5 Hrs .This unique trail takes you through
