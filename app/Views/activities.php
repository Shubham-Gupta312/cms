                                <p><small>Recreation for Bird Watchers...</small></p>

                                <p>The Cauvery Resorts is touted to be a haven for ornithologists and bird-lovers alike.
                                    Whether you are expert at bird behaviour or just a plain novice who admires the
                                    beauty of his feathered friends.The resort is the best place to explore and get in
                                    touch with various creatures of flight. The Indian sub-continent accounts for over
