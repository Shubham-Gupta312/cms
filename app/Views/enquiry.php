        $(document).ready(function () {

            $('body').on('keyup', ".onlynumbers", function (event) {
                this.value = this.value.replace(/[^[0-9]]*/gi, '');
            });

            $('body').on('keyup', ".onlychars", function (event) {
                this.value = this.value.replace(/[^[A-Za-z ]]*/gi, '');
            });

            jQuery(document).ready(function (e) {
                $('#enquiry').bootstrapValidator({
                    fields: {
                        'fname': {
                            validators: {
                                notEmpty: {
                                    message: "Please enter Name"
                                }
                            }
                        },
                        'roomtype': {
                            validators: {
                                notEmpty: {
                                    message: "Please select Room Type"
                                }
                            }
                        },
                        'package': {
                            validators: {
                                notEmpty: {
                                    message: "Please select Package"
                                }
                            }
                        },
                        'arrival_date': {
                            validators: {
                                notEmpty: {
                                    message: "Please Choose Arrival Date"
                                }
                            }
                        },
                        'departure_date': {
                            validators: {
                                notEmpty: {
                                    message: "Please Choose Departure Date"
                                }
                            }
                        },
                        'mobile': {
                            validators: {
                                notEmpty: {
                                    message: "Please enter Mobile Number"
                                }
                            }
                        },
                        'email': {
                            validators: {
                                notEmpty: {
                                    message: "Please enter email."
                                },
                                regexp: {
                                    regexp: /^[_A-Za-zA-Z0-9-]+(\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\.[a-z0-9-]+)*(\.[A-Za-z]{2,10})$/,
                                    message: 'Please enter valid email id'
                                }
                            }
                        },
                        'comments': {
                            validators: {
                                notEmpty: {
                                    message: "Please enter your Message"
                                }
                            }
                        },
                    }
                }).on('success.form.bv', function (e) {
                    e.preventDefault();
                    var $form = $(e.target);
                    var bv = $form.data('bootstrapValidator');
                    var formData = $form.serialize();
                    // console.log(formData);
                    // Use AJAX to submit form data
                    $.ajax({
                        url: "<?= base_url('enquiry') ?>",
                        type: 'POST',
                        data: formData,
                        success: function (response) {
                            // console.log(response);
                            if (response.status === 'true') {
                                // Reset form inputs
                                alert("Thank's for your response. We will get back to you soon!");
                                $('input, select, textarea').val('');
                            } else {
                                var msg = response.message;
                                $('.error').text(msg).css('color', 'red');
                            }
                        },
                        error: function (xhr, status, error) {
                            // Handle error
                            console.error(error);
                        }
                    });
                });
            });
        });
